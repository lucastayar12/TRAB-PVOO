    private void confMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_confMouseClicked
        // TODO add your handling code here:
        Voo voof = new Voo();
        Passageiro p = new Passageiro();
        Long vooId = Long.parseLong(cod.getText());
        Long pId = Long.parseLong(codP.getText());

        List<Voo> voos = daoVoo.lista();
        for (Voo voo : voos) {
            if (voo.getId() == vooId) {
                voof = voo;
            }
        }

        List<Passageiro> passageiros = daoPassageiro.lista();
        for (Passageiro passageiro : passageiros) {
            if(passageiro.getId() == pId)
            {
                p = passageiro;
            }
        }
        
        List<Assento> assentos = daoAssento.lista();
        for (Assento assento : assentos) {
            if (assento.getVoo().getId() == voof.getId() && assento.getCodAssento().equals(comboAssento.getSelectedItem())) {
                assento.setPassageiro(p);
                daoAssento.alteraP(assento);
            }
        }
        
        
        dispose();
    }//GEN-LAST:event_confMouseClicked

