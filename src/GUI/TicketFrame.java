    public TicketFrame(Voo voo, Passageiro p) {
        initComponents();

        List<Assento> assentos = daoAssento.lista();
        for (Assento ass : assentos) {

            if (ass.getVoo().getId() == voo.getId()) {
                this.comboAssento.addItem(ass.getCodAssento());
            }

        }

        cod.setText(String.valueOf(voo.getId()));
        codP.setText(String.valueOf(p.getId()));
        origem.setText(voo.getOrigem().getCidade());
        destino.setText(voo.getDestino().getCidade());
        valor.setText("R$ 100,00");

        Ticket tic = new Ticket();
        tic.setValor(100);
        tic.setVoo(voo);
        tic.setPassageiro(p);

        daoTicket.adiciona(tic);

    }

