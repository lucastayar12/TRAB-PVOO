    private void salvarMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_salvarMouseClicked
        // TODO add your handling code here:
        Ticket t = new Ticket();

        Long idVoo = Long.parseLong(codVoo.getText());
        List<Voo> voos = daoVoo.lista();
        for (Voo vo : voos) {
            if (vo.getId() == idVoo) {
                t.setVoo(vo);
            }
        }
        String np = (String) Combopassageiro.getSelectedItem();
        List<Passageiro> passageiros = daoPassageiro.lista();
        for (Passageiro p : passageiros) {
            if (p.getNome().equals(np)) {
                t.setPassageiro(p);
            }
        }
        int id = daoTicket.adiciona(t);
        List<Ticket> tickets = daoTicket.lista();

        for (Ticket tis : tickets) {
            if (id == tis.getId()) {
                this.tableModelTicket.add(tis);
            }
        }
    }//GEN-LAST:event_salvarMouseClicked

