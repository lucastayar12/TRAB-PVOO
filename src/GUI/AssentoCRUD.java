    private void excluirMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_excluirMouseClicked
        // TODO add your handling code here:
        Assento a = new Assento();
        a.setId(Long.parseLong(cod.getText()));
        daoAssento.exclui(a);
        for (int i = 0; i < tableModelAssento.getRowCount(); i++) {
            if (tableModelAssento.get(i).getId() == a.getId()) {
                tableModelAssento.remove(i);
            }
        }
    }//GEN-LAST:event_excluirMouseClicked

