    private Usuario logar() {

        String login = loginUser.getText();
        String senha = new String(loginSenha.getPassword());

        String senhaCriptografada = this.criptografa(senha);

        Usuario usuario = new Usuario();
        usuario.setLogin(login);
        usuario.setSenha(senhaCriptografada);
        return daoUsuarios.localizar(usuario);
    }

    private String criptografa(String original) {
        String senha = null;
        try {
            MessageDigest algorithm = MessageDigest.getInstance("SHA-256");
            byte messageDigest[] = algorithm.digest(original.getBytes("UTF-8"));

            StringBuilder hexString = new StringBuilder();
            for (byte b : messageDigest) {
                hexString.append(String.format("%02X", 0xFF & b));
            }
            senha = hexString.toString();

        } catch (NoSuchAlgorithmException | UnsupportedEncodingException e) {
            JOptionPane.showMessageDialog(null, "Erro: " + e.getMessage());
        }
        return senha;
    }
