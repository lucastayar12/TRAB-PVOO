    private void alterarMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_alterarMouseClicked
        // TODO add your handling code here:
        Ticket t = new Ticket();

        t.setId(Long.valueOf(cod.getText()));
        Long idVoo = Long.parseLong(valor.getText());
        List<Voo> voos = daoVoo.lista();
        for (Voo vo : voos) {
            if (vo.getId() == idVoo) {
                t.setVoo(vo);
            }
        }
        t.setValor(Double.valueOf(valor.getText()));
        String np = (String) Combopassageiro.getSelectedItem();
        List<Passageiro> passageiros = daoPassageiro.lista();
        for (Passageiro p : passageiros) {
            if (p.getNome().equals(np)) {
                t.setPassageiro(p);
            }
        }

        Long id = daoTicket.altera(t);

        List<Ticket> tickets = daoTicket.lista();
        for (Ticket tis : tickets) {
            if (id == tis.getId()) {
                this.tableModelTicket.edita(tis);
            }
        }
    }//GEN-LAST:event_alterarMouseClicked

