    private void excluirMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_excluirMouseClicked
        // TODO add your handling code here:
        Ticket t = new Ticket();
        t.setId(Long.parseLong(cod.getText()));
        daoTicket.exclui(t);
        for (int i = 0; i < tableModelTicket.getRowCount(); i++) {
            if (tableModelTicket.get(i).getId() == t.getId()) {
                tableModelTicket.remove(i);
            }
        }
    }//GEN-LAST:event_excluirMouseClicked

