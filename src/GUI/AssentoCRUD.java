    private void alterarMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_alterarMouseClicked
        // TODO add your handling code here:
        Assento a = new Assento();

        a.setId(Long.valueOf(cod.getText()));
        Long idVoo = Long.parseLong(voo.getText());
        List<Voo> voos = daoVoo.lista();
        for (Voo vo : voos) {
            if (vo.getId() == idVoo) {
                a.setVoo(vo);
            }
        }
        a.setCodAssento(codass.getText());
        String np = (String) Combopassageiro.getSelectedItem();
        List<Passageiro> passageiros = daoPassageiro.lista();
        for (Passageiro p : passageiros) {
            if (p.getNome().equals(np)) {
                a.setPassageiro(p);
            }
        }

        Long id = daoAssento.altera(a);

        List<Assento> assentos = daoAssento.lista();
        for (Assento ass : assentos) {
            if (id == ass.getId()) {
                this.tableModelAssento.edita(ass);
            }
        }
    }//GEN-LAST:event_alterarMouseClicked

